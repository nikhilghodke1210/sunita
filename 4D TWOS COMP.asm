LEA DX,MSG1
INT 21H

MOV BX,A
NEG BX

MOV CH,04H  ;NUMBER OF  DIGITS TO BE DISPLAYED
MOV CL,04H  ;NUMBER OF  BITS TO ROTATE

L1:
ROL BX,CL

MOV DL,BL
AND DL,0FH

CMP DL,09H
JBE L2

ADD DL,07H
L2:ADD DL,30H

MOV AH,02H
INT 21H

DEC CH
JNZ L1

MOV AH,4CH
